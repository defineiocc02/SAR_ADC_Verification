% =========================================================================
% run_false_freezing_analysis.m - 假死率 (False Freezing Rate) 分析
% =========================================================================
% 功能：分析 1-Flip (ALA) 和 2-Flip (HT-LA) 算法在高噪声下的假死率
%
% ============================================================================
% 物理背景 (Physical Background)
% ============================================================================
% 在 SAR ADC 的冗余量化阶段，比较器在热噪声环境下工作。当噪声较大时，
% 比较器可能产生"误判"，导致 DAC 控制逻辑出现"假翻转"。
%
% 【假死现象 (False Freezing)】
% - 1-Flip 算法 (ALA)：检测到 1 次比较器极性翻转即冻结 DAC
%   问题：如果这次翻转是由噪声毛刺引起的"假翻转"，DAC 会冻结在
%         距离真实残差零点较远的位置，导致估计误差增大
%
% - 2-Flip 算法 (HT-LA)：需要检测到 2 次翻转才冻结 DAC
%   优势：迟滞机制可以过滤掉单次噪声毛刺，只有连续两次极性变化
%         才被认为是有效信号，从而降低假死率
%
% 【假死率定义 (False Freezing Rate Definition)】
%   FFR = P(|freeze_res| > threshold)
%       = sum(abs(freeze_res) > threshold) / N_pts * 100 (%)
%
%   其中：
%   - freeze_res: 冻结时刻的追踪残差电压 (LSB)
%   - threshold: 假死判定阈值，默认 1.0 LSB
%   - 当 |freeze_res| > 1.0 LSB 时，认为发生了"假死"
%
% 【物理意义】
%   - freeze_res 理想情况下应接近 0（残差被正确追踪）
%   - 如果 freeze_res 较大，说明 DAC 在错误的位置冻结
%   - 假死率越高，算法在高噪声下的可靠性越差
%
% ============================================================================
% 算法对比 (Algorithm Comparison)
% ============================================================================
%   ┌─────────────┬──────────────────┬──────────────────────────────┐
%   │   算法      │    冻结条件      │           特点               │
%   ├─────────────┼──────────────────┼──────────────────────────────┤
%   │ 1-Flip(ALA) │ 1次极性翻转      │ 响应快，但易受噪声干扰       │
%   │ 2-Flip(HTLA)│ 2次极性翻转      │ 迟滞抗噪，假死率低           │
%   └─────────────┴──────────────────┴──────────────────────────────┘
%
% ============================================================================
% 输入参数 (Input Parameters)
% ============================================================================
%   无输入参数，所有配置在函数内部定义
%
% ============================================================================
% 输出 (Output)
% ============================================================================
%   1. Fig_5_False_Freezing_Rate.png - 假死率随噪声变化的折线图 (JSSC 风格)
%   2. Fig_5_False_Freezing_Rate.eps - EPS 格式图表（用于论文投稿）
%   3. Data_False_Freezing_Results.mat - 分析数据文件
%   4. 控制台输出 - 各噪声点下的假死率数据表格
%
% ============================================================================
% 参考文献 (References)
% ============================================================================
%   [1] Zhao et al., "A 16-bit 1-MS/s SAR ADC with asynchronous LSB averaging",
%       IEEE JSSC, 2024 - ALA 算法原始论文
%   [2] Miki et al., "Adaptive Tracking Average for SAR ADC", 2015
%
% ============================================================================
% 版本历史 (Version History)
% ============================================================================
%   v1.0 - 2026-03-04: 初始实现
%   v1.1 - 2026-03-04: 完善注释，添加物理背景说明
%
% 作者：AI Assistant
% 日期：2026-03
% =========================================================================

function run_false_freezing_analysis()
    %% ========================================================================
    % 步骤1: 参数设定
    %% ========================================================================
    % -------------------------------------------------------------------------
    % 仿真参数
    % -------------------------------------------------------------------------
    N_pts = 50000;                          % 蒙特卡洛样本数
                                            % 大样本数保证统计平滑性
                                            % 50000 样本可使假死率估计误差 < 0.5%
    
    N_red = 20;                             % 冗余周期数
                                            % 决定算法可用于追踪的时间窗口
                                            % N_red 越大，追踪越充分，但功耗越高
    
    sigma_range = linspace(0.4, 2.0, 17);   % 比较器噪声范围 (LSB)
                                            % 0.4 LSB: 低噪声，典型工作点
                                            % 2.0 LSB: 极端噪声，测试算法鲁棒性
                                            % 17 个点保证曲线平滑
    
    V_res_range = [-5.0, 5.0];              % 初始残差范围 (LSB)
                                            % 均匀分布模拟各种量化场景
                                            % ±5 LSB 覆盖大部分实际残差范围
    
    freeze_threshold = 1.0;                 % 假死判定阈值 (LSB)
                                            % 当 |freeze_res| > 1.0 LSB 时判定为假死
                                            % 1.0 LSB 是量化误差的边界
    
    % -------------------------------------------------------------------------
    % LUT 参数 (HT-LA 算法需要)
    % -------------------------------------------------------------------------
    LUT_MLE = zeros(1, N_red + 1);          % 简化 LUT
                                            % 假死率分析只关心冻结位置
                                            % 不需要精确的残差估计，LUT 置零即可
    
    %% ========================================================================
    % 步骤2: 初始化结果存储
    %% ========================================================================
    FFR_ALA = zeros(1, length(sigma_range));            % ALA 假死率 (%)
    FFR_HT_LA = zeros(1, length(sigma_range));          % HT-LA 假死率 (%)
    mean_freeze_ALA = zeros(1, length(sigma_range));    % ALA 平均冻结残差 (LSB)
    mean_freeze_HT_LA = zeros(1, length(sigma_range));  % HT-LA 平均冻结残差 (LSB)
    
    %% ========================================================================
    % 步骤3: 噪声扫描主循环
    %% ========================================================================
    fprintf('\n================================================================================\n');
    fprintf('  假死率分析 (False Freezing Rate Analysis)\n');
    fprintf('================================================================================\n');
    fprintf('  样本数: %d, 冗余周期: %d, 噪声范围: %.2f - %.2f LSB\n', ...
            N_pts, N_red, sigma_range(1), sigma_range(end));
    fprintf('  假死阈值: %.1f LSB\n\n', freeze_threshold);
    
    total_iters = length(sigma_range);
    
    for i_sigma = 1:length(sigma_range)
        sigma_th = sigma_range(i_sigma);
        
        fprintf('  [%d/%d] σ_n = %.2f LSB ... ', i_sigma, total_iters, sigma_th);
        
        % ---------------------------------------------------------------------
        % 生成随机初始残差（均匀分布）
        % ---------------------------------------------------------------------
        % V_res_LSB 表示初始残差电压，单位为 LSB
        % 均匀分布在 [-5, +5] LSB 范围内，模拟各种量化场景
        V_res_LSB = V_res_range(1) + (V_res_range(2) - V_res_range(1)) * rand(1, N_pts);
        
        % ---------------------------------------------------------------------
        % 生成漂移矩阵
        % ---------------------------------------------------------------------
        % RW_drift 模拟比较器输入偏移的随机游走漂移
        % 漂移幅度 = sigma_th * 0.3，表示低频漂移约为热噪声的 30%
        RW_drift = randn(N_pts, N_red) * (sigma_th * 0.3);
        
        % ---------------------------------------------------------------------
        % 运行 ALA (1-Flip) 算法
        % ---------------------------------------------------------------------
        % 输出参数：
        %   - est: 残差估计值（本分析不使用）
        %   - pwr_switch: 功耗指示（本分析不使用）
        %   - k_final: 比较+1 次数（本分析不使用）
        %   - freeze_res: 冻结时的残差电压（关键输出）
        [~, ~, ~, freeze_res_ALA] = run_ala(V_res_LSB, N_red, sigma_th, RW_drift);
        
        % ---------------------------------------------------------------------
        % 运行 HT-LA (2-Flip) 算法
        % ---------------------------------------------------------------------
        % HT-LA 需要额外的 LUT 参数
        [~, ~, ~, freeze_res_HT_LA] = run_htla(V_res_LSB, N_red, sigma_th, LUT_MLE, RW_drift);
        
        % ---------------------------------------------------------------------
        % 计算假死率
        % ---------------------------------------------------------------------
        % 假死判定条件：
        %   1. abs(freeze_res) > freeze_threshold (冻结位置偏离零点)
        %   2. freeze_res ~= 0 (排除全程未翻转的情况)
        %
        % 注意：freeze_res = 0 有两种可能：
        %   - 初始残差恰好为 0（极少）
        %   - 全程未发生翻转，DAC 未移动
        %   这两种情况都不应计入假死
        
        % ALA 假死判定
        false_freeze_ALA = (abs(freeze_res_ALA) > freeze_threshold) & (freeze_res_ALA ~= 0);
        FFR_ALA(i_sigma) = sum(false_freeze_ALA) / N_pts * 100;  % 转换为百分比
        
        % 计算平均冻结残差（仅统计发生冻结的样本）
        valid_freeze_ALA = freeze_res_ALA ~= 0;
        if any(valid_freeze_ALA)
            mean_freeze_ALA(i_sigma) = mean(abs(freeze_res_ALA(valid_freeze_ALA)));
        end
        
        % HT-LA 假死判定
        false_freeze_HT_LA = (abs(freeze_res_HT_LA) > freeze_threshold) & (freeze_res_HT_LA ~= 0);
        FFR_HT_LA(i_sigma) = sum(false_freeze_HT_LA) / N_pts * 100;
        
        valid_freeze_HT_LA = freeze_res_HT_LA ~= 0;
        if any(valid_freeze_HT_LA)
            mean_freeze_HT_LA(i_sigma) = mean(abs(freeze_res_HT_LA(valid_freeze_HT_LA)));
        end
        
        fprintf('ALA: %.2f%%, HT-LA: %.2f%%\n', FFR_ALA(i_sigma), FFR_HT_LA(i_sigma));
    end
    
    %% ========================================================================
    % 步骤4: 绘图 - 假死率随噪声变化 (JSSC 风格)
    %% ========================================================================
    fprintf('\n>>> 生成假死率分析图表...\n');
    
    % -------------------------------------------------------------------------
    % 图表参数设置
    % -------------------------------------------------------------------------
    fs_title = 14;                          % 标题字体大小
    fs_axis = 12;                           % 坐标轴标签字体大小
    fs_legend = 11;                         % 图例字体大小
    
    % -------------------------------------------------------------------------
    % 创建图形窗口
    % -------------------------------------------------------------------------
    figure('Position', [100, 100, 800, 600]);  % 800x600 像素
    hold on;
    grid on;                                % 显示网格
    box on;                                 % 显示边框
    
    % -------------------------------------------------------------------------
    % 绘制折线
    % -------------------------------------------------------------------------
    % ALA (1-Flip): 红色圆圈标记，实线
    plot(sigma_range, FFR_ALA, 'r-o', 'LineWidth', 2, 'MarkerSize', 8, ...
         'MarkerFaceColor', 'r', 'DisplayName', '1-Flip (ALA)');
    
    % HT-LA (2-Flip): 蓝色方块标记，实线
    plot(sigma_range, FFR_HT_LA, 'b-s', 'LineWidth', 2, 'MarkerSize', 8, ...
         'MarkerFaceColor', 'b', 'DisplayName', '2-Flip (HT-LA)');
    
    hold off;
    
    % -------------------------------------------------------------------------
    % 坐标轴设置
    % -------------------------------------------------------------------------
    xlabel('Comparator Noise $\sigma_n$ (LSB)', 'Interpreter', 'latex', 'FontSize', fs_axis);
    ylabel('False Freezing Rate (%)', 'Interpreter', 'latex', 'FontSize', fs_axis);
    title('False Freezing Rate vs. Comparator Noise', 'FontSize', fs_title, 'Interpreter', 'latex');
    
    % -------------------------------------------------------------------------
    % 图例设置
    % -------------------------------------------------------------------------
    legend('Location', 'northwest', 'FontSize', fs_legend, 'Interpreter', 'latex');
    
    % -------------------------------------------------------------------------
    % 网格和字体设置
    % -------------------------------------------------------------------------
    set(gca, 'GridAlpha', 0.3, 'MinorGridAlpha', 0.1);
    set(gca, 'FontName', 'Times New Roman', 'FontSize', fs_axis - 1, 'TickLabelInterpreter', 'latex');
    
    % -------------------------------------------------------------------------
    % Y 轴范围设置
    % -------------------------------------------------------------------------
    % 自动调整 Y 轴范围，留出 10% 的余量
    ylim([0, max(max(FFR_ALA), max(FFR_HT_LA)) * 1.1]);
    
    %% ========================================================================
    % 步骤5: 保存图表
    %% ========================================================================
    % 输出目录：项目根目录下的 Results 文件夹
    output_dir = fullfile(fileparts(mfilename('fullpath')), '..', '..', '..', 'Results');
    if ~exist(output_dir, 'dir')
        mkdir(output_dir);
    end
    
    % 保存 PNG 格式
    saveas(gcf, fullfile(output_dir, 'Fig_5_False_Freezing_Rate.png'));
    
    % 保存 EPS 格式（JSSC 投稿需要矢量图）
    try
        saveas(gcf, fullfile(output_dir, 'Fig_5_False_Freezing_Rate.eps'), 'epsc');
    catch
        warning('EPS 保存失败，跳过');
    end
    
    fprintf('    图表已保存至: %s\n', output_dir);
    
    %% ========================================================================
    % 步骤6: 输出统计摘要
    %% ========================================================================
    fprintf('\n================================================================================\n');
    fprintf('  假死率分析结果摘要\n');
    fprintf('================================================================================\n');
    fprintf('  %-12s | %-15s | %-15s | %-12s\n', 'σ_n (LSB)', 'ALA FFR (%)', 'HT-LA FFR (%)', '改善倍数');
    fprintf('  %s\n', repmat('-', 1, 65));
    
    for i = 1:length(sigma_range)
        % 计算改善倍数：ALA 假死率 / HT-LA 假死率
        % 改善倍数越大，说明 HT-LA 相比 ALA 的优势越明显
        improvement = FFR_ALA(i) / max(FFR_HT_LA(i), 0.01);  % 避免除零
        fprintf('  %-12.2f | %-15.2f | %-15.2f | %-12.1fx\n', ...
                sigma_range(i), FFR_ALA(i), FFR_HT_LA(i), improvement);
    end
    
    % -------------------------------------------------------------------------
    % 关键发现总结
    % -------------------------------------------------------------------------
    fprintf('\n  关键发现:\n');
    fprintf('  - ALA (1-Flip) 在高噪声下假死率显著增加\n');
    fprintf('  - HT-LA (2-Flip) 通过迟滞机制有效降低假死率\n');
    fprintf('  - 在 σ_n = %.2f LSB 时，HT-LA 假死率降低 %.1f 倍\n', ...
            sigma_range(end), FFR_ALA(end) / max(FFR_HT_LA(end), 0.01));
    
    %% ========================================================================
    % 步骤7: 保存数据
    %% ========================================================================
    data_file = fullfile(output_dir, 'Data_False_Freezing_Results.mat');
    save(data_file, 'sigma_range', 'FFR_ALA', 'FFR_HT_LA', 'mean_freeze_ALA', 'mean_freeze_HT_LA', ...
         'N_pts', 'N_red', 'freeze_threshold');
    fprintf('\n  数据已保存至: %s\n', data_file);
    
    fprintf('\n================================================================================\n');
    fprintf('  假死率分析完成！\n');
    fprintf('================================================================================\n');
end
