% =========================================================================
% run_dlr.m - DLR (Dynamic LSB Repeat - 动态 LSB 重复)
% =========================================================================
% 功能：实现基准技术 DLR (Dynamic LSB Repeat)，完整独立实现
% 
% 物理动作 (Physical Action)：
%   纯动态追踪（Moving Target）：
%   - 在 N_red 个周期内，DAC 每一个周期都根据比较器判决强制进行 ±1 LSB 的物理跳变
%   - DAC 一直在震荡，紧咬残差
%
% 数学输出 (Math Output)：
%   算法最终估计值 est 仅仅是 DAC 移动步数的整数累加
%   - 由于 DAC 一直在震荡，DLR 永远存在 ±1 LSB 的量化稳态误差
%   - 绝对无法输出带有小数的亚 LSB 精度
%
% 功耗 (Power)：
%   极差。每一个冗余周期都必须产生一次 pwr_switch 累加
%
% 输入参数：
%   V_res    - 目标残差电压 (1×N_pts)，需要估计的残差电压
%   N_red    - 冗余周期数，用于噪声平均的采样次数
%   sig_th   - 噪声阈值 (LSB)，比较器热噪声的标准差
%   RW_drift - 随机游走漂移矩阵 (N_pts × N_red)，模拟比较器输入偏移的随机漂移
%
% 输出参数：
%   est        - 数字估计值 (1×N_pts)，最终的残差估计结果（整数）
%   pwr_switch - 切换功耗指示 (1×N_pts)，每个周期都累加
%   k_final    - 最终"1"的计数 (1×N_pts)，冗余周期内比较器输出1的次数
%
% 算法特点：
%   - 优点：实现简单，无需复杂计算
%   - 缺点：整数输出，无法达到亚 LSB 精度；功耗最高
%   - 应用：作为功耗和精度的基准下限
%
% 作者：AI Assistant
% 日期：2025-03
% 版本历史：
%   v1.0 - 初始实现
%   v2.0 - 移除错误概率映射，恢复整数累加（正确物理机制）
%   v3.0 - 【关键修复】回归论文 Eq (9)，确保纯动态追踪逻辑
%          - DAC 每个周期都强制 ±1 LSB 跳变
%          - 估计值 est = Σ D，整数累加
% =========================================================================

function [est, pwr_switch, k_final] = run_dlr(V_res, N_red, sig_th, RW_drift)
    %% ========================================================================
    % 步骤1: 初始化变量
    %% ========================================================================
    nT = length(V_res);  % 样本数量
    
    % 物理状态变量
    V_track = V_res;                    % DAC 追踪电压
    dac_switched = zeros(1, nT);        % DAC 移动步数累加（整数）
    pwr_switch = zeros(1, nT);          % 功耗指示
    k_ones = zeros(1, nT);              % 比较器输出 1 的计数
    
    %% ========================================================================
    % 步骤2: 执行 N_red 次冗余比较（纯动态追踪）
    %% ========================================================================
    for step = 1:N_red
        % 获取当前周期的漂移值（转置为行向量）
        drift_val = RW_drift(:, step)';
        
        % 比较器判决（带物理热噪声与低频漂移）
        noise = sig_th * randn(1, nT);
        decision = V_track + drift_val + noise;
        
        % DAC 强制翻转：±1 LSB
        D = ones(1, nT);
        D(decision <= 0) = -1;
        
        % 累积 DAC 移动步数（整数）
        dac_switched = dac_switched + D;
        
        % 更新追踪电压
        V_track = V_track - D;
        
        % 功耗累加（每个周期都耗电）
        pwr_switch = pwr_switch + 1;
        
        % 统计比较器输出 1 的次数
        k_ones = k_ones + (D == 1);
    end
    
    %% ========================================================================
    % 步骤3: 输出整数估计值
    %% ========================================================================
    % DLR 的估计值是整数，无法达到亚 LSB 精度
    est = dac_switched;
    
    % 保存最终的"1"计数
    k_final = k_ones;
    
    %% ========================================================================
    % 算法执行流程总结：
    % 1. 每个周期 DAC 强制翻转 ±1 LSB
    % 2. 累积 DAC 移动步数作为估计值
    % 3. 输出整数，功耗满载
    % ========================================================================
end
