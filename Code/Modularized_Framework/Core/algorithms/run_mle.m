% =========================================================================
% run_mle.m - MLE (基准技术：最大似然估计) 算法实现
% =========================================================================
% 功能：实现基准技术 MLE (Maximum Likelihood Estimation)，完整独立实现
% 算法原理：对冗余周期内的比较器判决结果进行计数，通过查找表将计数映射到残差估计值
% 
% 输入参数：
%   V_res    - 目标残差电压向量 (1×N_pts)，需要估计的残差电压
%   N_red    - 冗余周期数，用于噪声平均的采样次数
%   sig_th   - 噪声阈值 (LSB)，比较器热噪声的标准差
%   LUT      - 查找表向量 (1×(N_red+1))，将k值映射到残差估计值
%   RW_drift - 随机游走漂移矩阵 (N_pts × N_red)，模拟比较器输入偏移的随机漂移
% 
% 输出参数：
%   est        - 数字估计值 (1×N_pts)，最终的残差估计结果
%   pwr_switch - 切换功耗指示 (1×N_pts)，MLE算法功耗为0（无切换）
%   k_final    - 最终"1"的计数 (1×N_pts)，冗余周期内比较器输出1的次数
%   freeze_res - 冻结时的残差 (1×N_pts)，MLE不使用冻结机制，全为0
% 
% 算法特点：
%   - 优点：实现简单，理论上最优（在高斯噪声下）
%   - 缺点：需要完整的N_red次比较，功耗较高
%   - 应用：作为其他算法性能的基准参考
% 
% 作者：AI Assistant
% 日期：2025-03
% 版本：v2.0
% =========================================================================

function [est, pwr_switch, k_final, freeze_res] = run_mle(V_res, N_red, sig_th, LUT, RW_drift)
    %% ========================================================================
    % 步骤1: 初始化变量
    %% ========================================================================
    nT = length(V_res);  % 样本数量
    
    % MLE 不进行前端搜索，直接对 N_red 次判决计数后查表
    pwr_switch = zeros(1, nT);  % 功耗指示，MLE无切换操作，全为0
    freeze_res = zeros(1, nT);   % 冻结残差，MLE无冻结机制，全为0
    k_ones = zeros(1, nT);       % 计数器，记录每个样本中比较器输出"1"的次数
    
    %% ========================================================================
    % 步骤2: 执行N_red次冗余比较
    %% ========================================================================
    for step = 1:N_red
        % 获取当前周期的漂移值（转置为行向量）
        drift_val = RW_drift(:, step)';  % 第step列的漂移值
        
        % 初始化比较器输出为1（默认假设信号大于0）
        D = ones(1, nT);
        
        % 模拟比较器判决：V_res + 漂移 + 噪声 <= 0 时输出-1
        % 噪声项：sig_th * randn(1, nT) - 生成高斯分布噪声
        D(V_res + drift_val + sig_th * randn(1, nT) <= 0) = -1;
        
        % 统计比较器输出"1"的次数（D == 1时为1，否则为0）
        k_ones = k_ones + (D == 1);
    end
    
    %% ========================================================================
    % 步骤3: 通过查找表获取残差估计
    %% ========================================================================
    % LUT的索引从1开始，所以k_ones需要加1
    % 例如：k_ones=0 → LUT(1)，k_ones=N_red → LUT(N_red+1)
    est = LUT(k_ones + 1);
    
    % 保存最终的"1"计数
    k_final = k_ones;
    
    %% ========================================================================
    % 算法执行流程总结：
    % 1. 对每个样本执行N_red次比较，每次加入随机噪声
    % 2. 统计每个样本的"1"输出次数k_ones
    % 3. 通过LUT(k_ones+1)查找对应的残差估计值
    % 4. 输出估计结果和相关统计信息
    % ========================================================================
end
