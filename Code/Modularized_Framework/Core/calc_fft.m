% =========================================================================
% calc_fft.m - FFT频谱分析引擎
% =========================================================================
% 功能：计算输出信号的FFT功率谱密度和SNDR（信噪失真比）
%       使用Blackman-Harris窗函数减少频谱泄漏
% 
% 输入参数：
%   Dout  - 输出数字码向量 (1×N_fft)，ADC量化后的输出码
%   Fs    - 采样率 (Hz)，ADC的采样频率
%   N_fft - FFT点数，通常为2的幂次（如8192, 16384等）
%   ADC   - ADC结构体，包含配置参数（本函数中未直接使用，保留接口一致性）
%   Fin   - 输入信号频率 (Hz)，用于定位信号bin位置
% 
% 输出参数：
%   f_axis   - 频率轴向量 (Hz)，单边谱频率范围 [0, Fs/2]
%   PSD_val  - 功率谱密度值 (dBFS)，单边谱功率密度
%   sndr_val - SNDR值 (dB)，信噪失真比
% 
% 算法流程：
%   1. 加Blackman-Harris窗 → 2. FFT变换 → 3. 计算功率谱
%   4. 转换为单边谱 → 5. 计算SNDR（信号功率/噪声功率）
% 
% 作者：AI Assistant
% 日期：2025-03
% 版本：v2.0
% =========================================================================

function [f_axis, PSD_val, sndr_val] = calc_fft(Dout, Fs, N_fft, ADC, Fin)
    %% ========================================================================
    % 步骤1: 加窗处理 - 使用Blackman-Harris窗减少频谱泄漏
    %% ========================================================================
    % Blackman-Harris窗具有极低的旁瓣（-92dB），适合高精度频谱分析
    win = blackmanharris(N_fft)';  % 生成窗函数并转置为行向量
    
    % 计算窗函数相干增益（Coherent Gain），用于功率补偿
    % 相干增益 = 窗函数均值，FFT后需要除以该值进行补偿
    win_cg = sum(win) / N_fft;
    
    % 对信号加窗并进行FFT变换
    % Dout .* win: 时域信号与窗函数相乘（逐点相乘）
    % fft(): 快速傅里叶变换，输出双边谱
    Y = fft(Dout .* win);
    
    %% ========================================================================
    % 步骤2: 计算双边功率谱
    %% ========================================================================
    % P2: 双边功率谱，包含正负频率成分
    % 除以N_fft进行归一化，再取模平方得到功率
    P2 = abs(Y / N_fft).^2;
    
    %% ========================================================================
    % 步骤3: 转换为单边功率谱
    %% ========================================================================
    % 取双边谱的前半部分（正频率部分）
    P1 = P2(1:N_fft/2+1);
    
    % 将非直流和非奈奎斯特频率的分量乘以2
    % 原因：双边谱的能量分布在正负频率，单边谱需要合并
    % P1(1)是直流分量，P1(end)是奈奎斯特频率，这两个不乘以2
    P1(2:end-1) = 2 * P1(2:end-1);
    
    %% ========================================================================
    % 步骤4: 生成频率轴并计算功率谱密度(dBFS)
    %% ========================================================================
    % 频率轴：从0到Fs/2，共N_fft/2+1个点
    f_axis = Fs * (0:(N_fft/2)) / N_fft;
    
    % 功率谱密度转换为dBFS（相对于满量程的分贝值）
    % 注意：P1已经是补偿后的功率谱，不需要再除以win_cg^2
    % 因为信号和噪声被同样衰减，SNDR计算时比值不变
    PSD_val = 10 * log10(P1);
    
    %% ========================================================================
    % 步骤5: 计算SNDR（信噪失真比）
    %% ========================================================================
    % SNDR = 10*log10(信号功率 / (噪声功率 + 谐波失真功率))
    % 重要：使用未补偿的P1计算，因为信号和噪声衰减相同，比值不变
    
    % 5.1 定位信号主频的bin位置
    % 公式：bin = round(Fin/Fs * N_fft) + 1
    % MATLAB索引从1开始，频率轴从0开始
    sig_bin = round(Fin * N_fft / Fs) + 1;
    
    % 边界保护：确保bin位置在有效范围内 [3, N_fft/2-1]
    % 留足±2bins的边界，避免DC和奈奎斯特频率
    sig_bin = max(3, min(sig_bin, N_fft/2 - 1));
    
    % 5.2 计算信号功率
    % Blackman-Harris窗主瓣宽度约8 bins，信号能量分布在±4 bins范围
    % 使用±4 bins捕获完整的信号能量
    sig_bins = max(2, sig_bin-4) : min(N_fft/2, sig_bin+4);
    P_signal = sum(P1(sig_bins));  % 信号bin的功率和
    
    % 5.3 计算噪声+失真功率
    % 方法：总功率 - 信号功率
    % 包含：热噪声、量化噪声、谐波失真等
    P_total = sum(P1);  % 总功率（未补偿）
    P_noise_dist = P_total - P_signal;  % 噪声+失真功率
    
    % 5.4 计算SNDR值
    if P_noise_dist > 0
        % 正常情况：SNDR = 10*log10(信号功率/噪声功率)
        sndr_val = 10 * log10(P_signal / P_noise_dist);
    else
        % 理想情况：无噪声时SNDR设为100dB（上限值）
        sndr_val = 100;
    end
end
