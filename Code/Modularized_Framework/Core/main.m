% =========================================================================
% main.m - 主运行脚本
% =========================================================================
% 功能：SAR ADC 残差估计算法验证框架
% 
% 运行模式说明：
%   RUN_MODE = 1: 完整验证框架 (config -> run_monte_carlo -> generate_figures -> generate_report)
%   RUN_MODE = 2: 多维度算法对比评估 (run_algorithm_comparison)
%   RUN_MODE = 3: 假死率分析 (run_false_freezing_analysis)
% =========================================================================

clc; clear; close all;

%% ========================================================================
%% 选择运行模式
%% ========================================================================
RUN_MODE = 2;  % 1: 完整验证框架, 2: 算法对比评估, 3: 假死率分析

if RUN_MODE == 1
    %% ========================================================================
    %% 模式1: 完整验证框架 (已注释)
    %% ========================================================================
    cfg = config();
    results = run_monte_carlo(cfg);
    generate_figures(cfg, results);
    generate_report(cfg, results);
    fprintf('>>> 验证框架运行完成！\n');
    
elseif RUN_MODE == 2
    %% ========================================================================
    %% 模式2: 算法对比评估
    %% ========================================================================
    fprintf('================================================================================\n');
    fprintf('  运行模式: 多维度算法对比评估\n');
    fprintf('================================================================================\n\n');
    
    run_algorithm_comparison();
    
    fprintf('\n======================================================================\n');
    fprintf('  对比评估完成！\n');
    fprintf('======================================================================\n');
    
elseif RUN_MODE == 3
    %% ========================================================================
    %% 模式3: 假死率分析 (False Freezing Rate Analysis)
    %% ========================================================================
    fprintf('================================================================================\n');
    fprintf('  运行模式: 假死率分析\n');
    fprintf('================================================================================\n');
    fprintf('  分析对象: 1-Flip (ALA) vs 2-Flip (HT-LA)\n');
    fprintf('  目的: 评估高噪声下的假死率差异\n');
    fprintf('================================================================================\n\n');
    
    run_false_freezing_analysis();
    
    fprintf('\n======================================================================\n');
    fprintf('  假死率分析完成！\n');
    fprintf('======================================================================\n');
end
